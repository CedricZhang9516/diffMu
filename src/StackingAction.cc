G4ClassificationOfNewTrack
StackingAction::ClassifyNewTrack(const G4Track* track)
{
  //keep primary particle
  if (track->GetParentID() == 0) return fUrgent;

  //kill secondary neutrino (or other light particles, depending on mass)
  if (track->GetDefinition()->GetPDGMass() < 0.1*CLHEP::MeV) return fKill;
  return fUrgent;
}

